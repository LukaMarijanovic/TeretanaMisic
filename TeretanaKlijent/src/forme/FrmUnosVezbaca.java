    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:            
    try{
        String ime = jTextFieldIme.getText();
        String prezime = jTextFieldPrezime.getText();
        double visina = Double.parseDouble(jTextFieldVisina.getText());
        double tezina = Double.parseDouble(jTextFieldTezina.getText());
        int clanarina = Integer.parseInt(jTextFieldClanarina.getText());
        
        
            if (ime == null || ime.isEmpty()) {
                prikaziPorukuGreske("Niste uneli ime vezbaca!");
                return;
            }

            if (prezime == null || prezime.isEmpty()) {
                prikaziPorukuGreske("Niste uneli prezime vezbaca!");
                return;
            }
            
             if (jTextFieldVisina.getText() == null || jTextFieldVisina.getText().isEmpty()) {

                prikaziPorukuGreske("Niste uneli visinu vezbaca!");
                return;
            }

            if (jTextFieldTezina.getText() == null || jTextFieldTezina.getText().isEmpty()) {
                prikaziPorukuGreske("Niste uneli tezinu vezbaca!");
                return;
            }
            
             if (jTextFieldClanarina.getText() == null || jTextFieldClanarina.getText().isEmpty()) {
                prikaziPorukuGreske("Niste uneli clanarinu vezbaca!");
                return;
            }
             
             Vezbac vezbac = new Vezbac();
             vezbac.setIme(ime);
             vezbac.setPrezime(prezime);
             vezbac.setVisina(visina);
             vezbac.setTezina(tezina);
             vezbac.setClanarina(clanarina);
             
             if(Komunikacija.getInstance().getAktivan_sk() == Konstante.OPERACIJA_DODAVANJA_VEZBACA){
                 Komunikacija.getInstance().sacuvajVezbaca(vezbac);
                prikaziPorukuObavestenja("Sistem je zapamtio novog vezbaca!");
                obrisiPodatkeIzPolja();
             }
             
              if(Komunikacija.getInstance().getAktivan_sk() == Konstante.OPERACIJA_IZMENI_VEZBACE){
                  vezbac.setId(v.getVezbacID());
                 Komunikacija.getInstance().izmeniVezbaca(vezbac);
                prikaziPorukuObavestenja("Sistem je zapamtio novog vezbaca!");
                obrisiPodatkeIzPolja();
             }
             
              }
              catch(Exception ex){
                   if (ex.getMessage() != null && ex.getMessage().equals(Konstante.PORUKA_DA_VEC_POSTOJI)) {
                prikaziPorukuGreske("Već je unet ovakav vezbac!");
                return;
              }
                    prikaziPorukuGreske("Sistem ne može da zapamti vezbaca!");
              }
    }//GEN-LAST:event_jButton1ActionPerformed

