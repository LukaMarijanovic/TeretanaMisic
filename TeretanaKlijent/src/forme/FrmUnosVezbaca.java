public void srediFormuPremaSK(){
    int aktivanSK = Komunikacija.getInstance().getAktivan_sk();
    
    if(aktivanSK == Konstante.SK_DODAVANJE){
        srediFormu();
        this.setTitle("Unos novog vezbaca");
    }
    if(aktivanSK == Konstante.SK_IZMENA){
        srediFormu();
        prikaziPodatkeVezbaca(Komunikacija.getInstance().getVezbac());
        this.setTitle("Izmena vezbaca");
    }
    
    if(aktivanSK == Konstante.SK_PRIKAZ){
            jTextFieldIme.setEditable(false);
            jTextFieldPrezime.setEditable(false);
            jTextFieldVisina.setEditable(false);
            jTextFieldTezina.setEditable(false);
            jTextFieldClanarina.setEditable(false);
            jButton1.setVisible(false);

            
            prikaziPodatkeVezbaca(Komunikacija.getInstance().getVezbac());
        }
    
}
    
    public void srediFormu(){
    jTextFieldIme.setEnabled(true);
    jTextFieldPrezime.setEnabled(true);
    jTextFieldVisina.setEnabled(true);
    jTextFieldTezina.setEnabled(true);
    jTextFieldClanarina.setEnabled(true);
}

private void prikaziPorukuGreske(String poruka) {
        JOptionPane.showMessageDialog(this, poruka, "Greška!", JOptionPane.ERROR_MESSAGE);
    }

    private void prikaziPorukuObavestenja(String poruka) {
        JOptionPane.showMessageDialog(this, poruka, "Obaveštenje!", JOptionPane.INFORMATION_MESSAGE);
    }
    
     private void obrisiPodatkeIzPolja() {
        
        
        jTextFieldIme.setText("");
        jTextFieldPrezime.setText("");
        jTextFieldVisina.setText("");
        jTextFieldTezina.setText("");
        jTextFieldClanarina.setText("");
        
    }
     
     private void prikaziPodatkeVezbaca(Vezbac vezbac){
        jTextFieldIme.setText(vezbac.getIme());
        jTextFieldPrezime.setText(vezbac.getPrezime());
        jTextFieldVisina.setText(Double.toString(vezbac.getVisina()));
        jTextFieldTezina.setText(Double.toString(vezbac.getTezina()));
        jTextFieldClanarina.setText(Integer.toString(vezbac.getClanarina()));
        
     }

